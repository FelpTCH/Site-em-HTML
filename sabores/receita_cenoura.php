      <h1>Bolo de Cenoura</h1>
      <h3>Ingredientes:</h3>
      <ul>
        <li>3 cenouras médias picadas</li>
        <li>3 ovos</li>
        <li>1 xícara de óleo</li>
        <li>2 xícaras de açúcar</li>
        <li>2 e 1/2 xícaras de farinha de trigo</li>
        <li>1 colher de sopa de fermento em pó</li>
      </ul>

      <h3>Modo de Preparo:</h3>
      <p>
        Bata no liquidificador a cenoura, os ovos e o óleo. Em seguida, misture o açúcar, a farinha e o fermento em uma tigela e adicione o conteúdo do liquidificador. Mexa bem. Leve ao forno pré-aquecido a 180ºC por cerca de 40 minutos.
      </p>

      <h3>Cobertura (opcional):</h3>
      <p>
        Derreta 1 colher de manteiga, 3 colheres de chocolate em pó, 1 xícara de açúcar e 3 colheres de leite. Mexa bem e despeje sobre o bolo ainda quente.
      </p>
